def fizz_buzz(n):
    # Проходим по числам от 1 до n включительно
    for i in range(1, n + 1):
        # Сначала проверяем деление и на 3, и на 5
        if i % 3 == 0 and i % 5 == 0:
            print("FizzBuzz")
        # Если не выполнилось первое условие, проверяем, делится ли только на 3
        elif i % 3 == 0:
            print("Fizz")
        # Если не выполнились первые два, проверяем, делится ли только на 5
        elif i % 5 == 0:
            print("Buzz")
        # Если ни одно условие не подошло, печатаем само число
        else:
            print(i)


fizz_buzz(20)
